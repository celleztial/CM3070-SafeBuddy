// Calculate a user's reading progress (completed count vs. total available) in one language
export const calculateProgress = (completedMaterials, language = 'en') => {
  const materials = readingMaterials[language] || [];
  const totalMaterials = materials.length;
  const completed = completedMaterials.filter(id => 
    materials.some(material => material.id === id)
  ).length;
  return {
    percentage: Math.round((completed / totalMaterials) * 100),
    completed,
    total: totalMaterials
  };
};

export const calculateTotalPoints = (completedMaterials) => {
  let totalPoints = 0;
  
  // Check all languages for completed materials
  Object.keys(readingMaterials).forEach(language => {
    const materials = readingMaterials[language] || [];
    completedMaterials.forEach(materialId => {
      const material = materials.find(m => m.id === materialId);
      if (material) {
        totalPoints += material.points;
      }
    });
  });
  
  return totalPoints;
};

